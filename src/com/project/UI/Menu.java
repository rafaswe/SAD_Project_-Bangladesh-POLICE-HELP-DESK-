    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        // TODO add your handling code here:
        if (Vname.getText().isEmpty() || Vphone.getText().isEmpty() || Vnationality.getText().isEmpty() || Vaddress.getText().isEmpty() || VcrimeName.getText().isEmpty() || VcrimeTime.getText().isEmpty() || VcrimePlace.getText().isEmpty() || Vdetails.getText().isEmpty()) {
            HeadingPanel.setVisible(true);
            MenuPanel.setVisible(true);
            AboutPanel.setVisible(false);
            PoliceNumberPanel.setVisible(false);
            AdminPanel.setVisible(false);
            EditPanel.setVisible(false);
            SuspectPanel.setVisible(false);
            VictimSupportPanel.setVisible(true);
            JOptionPane.showMessageDialog(null, "Please Enter All information.");
        } else {
            HeadingPanel.setVisible(true);
            MenuPanel.setVisible(true);
            AboutPanel.setVisible(false);
            PoliceNumberPanel.setVisible(false);
            AdminPanel.setVisible(false);
            EditPanel.setVisible(false);
            VictimSupportPanel.setVisible(false);
              victimName=Vname.getText();
            if(Victim.checkUniqueUID(victimName)){
            Victim.vicInfo(Vname.getText(), Integer.parseInt(Vage.getText()), Vphone.getText(), Vnid.getText(), Vnationality.getText(), Vaddress.getText(),LogInForm.uname);
            Crime.crimeInfo(VcrimeName.getText(), VcrimeTime.getText(), VcrimePlace.getText(), Vdetails.getText(),Vname.getText(),LogInForm.uname);
            SuspectPanel.setVisible(true);
        }
            else
                JOptionPane.showMessageDialog(null,"Name Must be unique","Invalid info", 1);
        }
    }//GEN-LAST:event_jButton4ActionPerformed

